import math
r = float(input("Nhập bán kính của hình tròn:"))
chu_vi = 2 * math.pi * r
dien_tich = math.pi * r * r
print("Chu vi của hình tròn là:",chu_vi)
print("Diện tích của hình tròn là:",dien_tich)

